package com.screen.mirror.Models;

public class Main_Menu_Model {
    private int icon;
    private String name;

    public Main_Menu_Model(int icon, String name) {
        this.icon = icon;
        this.name = name;
    }

    public int getIcon() {
        return icon;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
